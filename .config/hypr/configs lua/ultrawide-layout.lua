-- /* ---- 💫 Layout Ultrawide 3 colonnes 💫 ---- */
-- Fichier : ~/.config/hypr/ultrawide-layout.lua
--
-- COMPORTEMENT :
--   - Fenêtre 1 → colonne 1
--   - Fenêtre 2 → colonne 2
--   - Fenêtre 3 → colonne 3  (3 colonnes égales 1/3 chacune)
--   - Fenêtre 4+ → se place dans la colonne de la fenêtre active
--                  et divise la hauteur en parts égales (comme dwindle)
--   - SUPER + KP_1/2/3 → déplacer la fenêtre active vers une colonne
--   - SUPER + X        → cycle colonne suivante
 
local COL_COUNT   = 3
local window_cols = {}  -- { [address] = col_index }
local open_count  = 0   -- compteur pour les 3 premières fenêtres
 
-- ── Assignation de colonne à l'ouverture ──────────────────────────────────────
hl.on("window.open", function(w)
    if not w or not w.address then return end
 
    open_count = open_count + 1
 
    if open_count <= COL_COUNT then
        -- 3 premières fenêtres → une par colonne dans l'ordre
        window_cols[w.address] = open_count
    else
        -- Fenêtre 4+ → colonne de la fenêtre active
        local active = hl.get_active_window()
        if active and window_cols[active.address] then
            window_cols[w.address] = window_cols[active.address]
        else
            -- Fallback : colonne avec le moins de fenêtres
            local counts = { 0, 0, 0 }
            for _, col in pairs(window_cols) do
                if counts[col] then counts[col] = counts[col] + 1 end
            end
            local min_col, min_val = 1, counts[1]
            for i = 2, COL_COUNT do
                if counts[i] < min_val then min_col, min_val = i, counts[i] end
            end
            window_cols[w.address] = min_col
        end
    end
end)
 
-- ── Nettoyage à la fermeture ──────────────────────────────────────────────────
hl.on("window.close", function(w)
    if w and w.address then
        window_cols[w.address] = nil
        open_count = math.max(0, open_count - 1)
    end
end)
 
-- ── Layout ────────────────────────────────────────────────────────────────────
hl.layout.register("ultrawide", {
 
    recalculate = function(ctx)
        local area    = ctx.area
        local targets = ctx.targets
        if #targets == 0 then return end
 
        -- Fenêtres sans colonne assignée (ex: au reload) → colonne selon position
        for _, t in ipairs(targets) do
            local addr = t.window and t.window.address
            if addr and not window_cols[addr] then
                -- Compter les colonnes existantes pour assigner intelligemment
                local counts = { 0, 0, 0 }
                for _, col in pairs(window_cols) do
                    if counts[col] then counts[col] = counts[col] + 1 end
                end
                local min_col, min_val = 1, counts[1]
                for i = 2, COL_COUNT do
                    if counts[i] < min_val then min_col, min_val = i, counts[i] end
                end
                window_cols[addr] = min_col
            end
        end
 
        -- Grouper par colonne
        local cols = {}
        for i = 1, COL_COUNT do cols[i] = {} end
 
        for _, t in ipairs(targets) do
            local addr = t.window and t.window.address
            local col  = (addr and window_cols[addr]) or 1
            col        = math.max(1, math.min(COL_COUNT, col))
            table.insert(cols[col], t)
        end
 
        -- Placer : 3 colonnes égales, fenêtres empilées verticalement dans chaque colonne
        local col_w = area.w / COL_COUNT
 
        for ci = 1, COL_COUNT do
            local wins = cols[ci]
            local nw   = #wins
            if nw > 0 then
                local row_h = area.h / nw
                for ri, t in ipairs(wins) do
                    t:place({
                        x = area.x + (ci - 1) * col_w,
                        y = area.y + (ri - 1) * row_h,
                        w = col_w,
                        h = row_h,
                    })
                end
            end
        end
    end,
 
    layout_msg = function(ctx, msg)
        local active = hl.get_active_window()
        if not active then return false end
 
        -- "col1" / "col2" / "col3" → déplacer vers cette colonne
        local col = tonumber(msg:match("^col(%d)$"))
        if col and col >= 1 and col <= COL_COUNT then
            window_cols[active.address] = col
            return true
        end
 
        -- "swap_col" → colonne suivante
        if msg == "swap_col" then
            local current = window_cols[active.address] or 1
            window_cols[active.address] = (current % COL_COUNT) + 1
            return true
        end
 
        return false
    end,
})
 
