Workspace bindings on non-QWERTY layouts
Keys used for keybinds need to be accessible without any modifiers in your layout. For instance, the [French AZERTY](https://en.wikipedia.org/wiki/AZERTY) layout uses SHIFT + unmodified key to write 0-9 numbers. As such, the workspace keybinds for this layout need to use the names of the unmodified keys , and will not work when using the 0-9 numbers.
